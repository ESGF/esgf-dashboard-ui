package org.esg.node.dataArchive.bean;

public class DataArchive {
	
    private String datasetsNumber;
    private String totalSize;
    
	public String getDatasetsNumber() {
		return datasetsNumber;
	}
	public void setDatasetsNumber(String datasetsNumber) {
		this.datasetsNumber = datasetsNumber;
	}
	public String getTotalSize() {
		return totalSize;
	}
	public void setTotalSize(String totalSize) {
		this.totalSize = totalSize;
	}
}
