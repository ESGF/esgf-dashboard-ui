package org.esg.node.overallView;

public class OverallViewBean {
	
    private String datasets;
    private String datanodes;
    private String countries;
    private String projects;
    private String downloads;
    private String downloadedData;
    
	public String getDatasets() {
		return datasets;
	}
	public void setDatasets(String datasets) {
		this.datasets = datasets;
	}
	public String getDatanodes() {
		return datanodes;
	}
	public void setDatanodes(String datanodes) {
		this.datanodes = datanodes;
	}
	public String getCountries() {
		return countries;
	}
	public void setCountries(String countries) {
		this.countries = countries;
	}
	public String getProjects() {
		return projects;
	}
	public void setProjects(String projects) {
		this.projects = projects;
	}
	public String getDownloads() {
		return downloads;
	}
	public void setDownloads(String downloads) {
		this.downloads = downloads;
	}
	public String getDownloadedData() {
		return downloadedData;
	}
	public void setDownloadedData(String downloadedData) {
		this.downloadedData = downloadedData;
	}
}
